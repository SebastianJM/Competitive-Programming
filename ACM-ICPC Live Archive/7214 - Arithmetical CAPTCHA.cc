#include <bits/stdc++.h>

using namespace std;

int v[4],n;

int main() 
{
	cin>>n;
	int t=1;
	while(n--)
	{
		cin>>v[0]>>v[1]>>v[2]>>v[3];
		cout<<"Case #"<<t++<<": ";
		if(v[0]==v[1] && v[1]==v[2] && v[2]==v[3])
			cout<<v[0]<<" = "<<v[1]<<" = "<<v[2]<<" = "<<v[3]<<endl;
			
		else if(v[0]+v[1]==v[2] && v[2]==v[3])
			cout<<v[0]<<" + "<<v[1]<<" = "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]-v[1]==v[2] && v[2]==v[3])
			cout<<v[0]<<" - "<<v[1]<<" = "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]==v[1]+v[2] && v[0]==v[3])
			cout<<v[0]<<" = "<<v[1]<<" + "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]==v[1]-v[2] && v[0]==v[3])
			cout<<v[0]<<" = "<<v[1]<<" - "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[2]+v[3]==v[0] && v[0]==v[1])
			cout<<v[0]<<" = "<<v[1]<<" = "<<v[2]<<" + "<<v[3]<<endl;
		else if(v[2]-v[3]==v[0] && v[0]==v[1])
			cout<<v[0]<<" = "<<v[1]<<" = "<<v[2]<<" - "<<v[3]<<endl;
		
		else if(v[0]+v[1]==v[2]+v[3])
			cout<<v[0]<<" + "<<v[1]<<" = "<<v[2]<<" + "<<v[3]<<endl;
		else if(v[0]+v[1]==v[2]-v[3])
			cout<<v[0]<<" + "<<v[1]<<" = "<<v[2]<<" - "<<v[3]<<endl;
		else if(v[0]-v[1]==v[2]+v[3])
			cout<<v[0]<<" - "<<v[1]<<" = "<<v[2]<<" + "<<v[3]<<endl;
		else if(v[0]-v[1]==v[2]-v[3])
			cout<<v[0]<<" - "<<v[1]<<" = "<<v[2]<<" - "<<v[3]<<endl;
			
		else if(v[0]+v[1]+v[2]==v[3])
			cout<<v[0]<<" + "<<v[1]<<" + "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]+v[1]-v[2]==v[3])
			cout<<v[0]<<" + "<<v[1]<<" - "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]-v[1]-v[2]==v[3])
			cout<<v[0]<<" - "<<v[1]<<" - "<<v[2]<<" = "<<v[3]<<endl;
		else if(v[0]-v[1]+v[2]==v[3])
			cout<<v[0]<<" - "<<v[1]<<" + "<<v[2]<<" = "<<v[3]<<endl;
		
		else if(v[0]==v[1]+v[2]+v[3])
			cout<<v[0]<<" = "<<v[1]<<" + "<<v[2]<<" + "<<v[3]<<endl;
		else if(v[0]==v[1]+v[2]-v[3])
			cout<<v[0]<<" = "<<v[1]<<" + "<<v[2]<<" - "<<v[3]<<endl;
		else if(v[0]==v[1]-v[2]+v[3])
			cout<<v[0]<<" = "<<v[1]<<" - "<<v[2]<<" + "<<v[3]<<endl;
		else if(v[0]==v[1]-v[2]-v[3])
			cout<<v[0]<<" = "<<v[1]<<" - "<<v[2]<<" - "<<v[3]<<endl;
	}
	return 0;
}